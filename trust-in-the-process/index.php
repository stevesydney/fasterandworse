                    <h1 itemprop="title">Trust in the process, it's worth the wait</h1>
                    <time itemprop="dateCreated pubdate datePublished" datetime="2019-03-26">March 26, 2019</time>
                </header>
                <main>
                    <p>Done is not better than perfect.</p>
                    <p>It's a mantra pushed in startups everywhere, variations of the phrase "The best is the enemy of the good".</p>
                    <p>The idea being that we sometimes get so caught up in our search for perfection that we cripple our ability to create anything at all.</p>
                    <p>It is a powerful tool for aligning people to imaginary deadlines. The paranoia of being slower than the others to get something out there. We're so sure that being first is the most important factor.</p>
                    <p>The power of the concept is its ambiguity. How do we define "complete", "done", "finished" or more accurately, "good enough"?</p>
                    <p>How do we define "perfect", "the best"?</p>
                    <p>It's a world where entrepreneurs idolise the creators of finely crafted, iconic products of the past.</p>
                    <p>We throw around words like "design", "user experience", "empathy" while also pushing this culture of corner-cutting and thoughtless iteration.</p>
                    <p>In a land of paradoxes where we create design systems and write a blog post about how they changed the way we work. A year later, web technologies have evolved and our obsolete design system is holding us back.</p>
                    <p>Our methods for code and design collaboration penalise any individual who attempts to achieve too much in one sitting. The iterations and pull requests must be small and readable so our colleagues can properly review them.</p>
                    <p>We're not stifling creativity in general, we're stifling the ability for an individual to solve interconnected problems in creative ways.</p>
                    <p itemprop="author"><a href="mailto:sfarrugia@gmail.com">Stephen Farrugia</a></p>
